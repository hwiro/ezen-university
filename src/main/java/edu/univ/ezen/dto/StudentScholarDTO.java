package edu.univ.ezen.dto;

public class StudentScholarDTO {
	private int num;
	private String student_id;
	private String scholar_year;
	private String semester;
	private String scholar_name;
	private String price;
	private String get_method;
	
	public StudentScholarDTO() {
		
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getStudent_id() {
		return student_id;
	}

	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}

	public String getScholar_year() {
		return scholar_year;
	}

	public void setScholar_year(String scholar_year) {
		this.scholar_year = scholar_year;
	}

	public String getSemester() {
		return semester;
	}

	public void setSemester(String semester) {
		this.semester = semester;
	}

	public String getScholar_name() {
		return scholar_name;
	}

	public void setScholar_name(String scholar_name) {
		this.scholar_name = scholar_name;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getGet_method() {
		return get_method;
	}

	public void setGet_method(String get_method) {
		this.get_method = get_method;
	}
	
	
	
}
